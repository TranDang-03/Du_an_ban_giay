    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        String madongSP = this.txtMa.getText();
        String tendongSP = this.txtTen.getText();
        int trangThai = Integer.parseInt(cbbdsp.getSelectedItem().toString());

        Model.DongSp dsp = new Model.DongSp(0, madongSP, tendongSP, trangThai);

        boolean a = this.service.insert(dsp);

        if (a == true) {
            JOptionPane.showMessageDialog(rootPane, "Thêm thành công");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Thêm thất bại");
            return;
        }

        this.clear();
        this.loadTable();
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int id = Integer.parseInt(txtID.getText());
        String maDongSP = this.txtMa.getText();
        String tenDongSP = this.txtTen.getText();
        int trangThai = Integer.parseInt(cbbdsp.getSelectedItem().toString());

        Model.DongSp dsp = new Model.DongSp(id, maDongSP, tenDongSP, trangThai);

        boolean s = this.service.update(dsp);

        if (s == true) {
            JOptionPane.showMessageDialog(rootPane, "Sửa thành công");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Sửa thất bại");
            return;
        }

        this.clear();
        this.loadTable();
    }//GEN-LAST:event_btnSuaActionPerformed

