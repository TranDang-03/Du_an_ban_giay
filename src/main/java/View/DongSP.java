    private void tbldspMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbldspMouseClicked
        int index = this.tbldsp.getSelectedRow();

        String id = model.getValueAt(index, 0).toString();
        String ma = model.getValueAt(index, 1).toString();
        String ten = model.getValueAt(index, 2).toString();
        String trangThai = model.getValueAt(index, 3).toString();

        this.txtID.setText(id);
        this.txtMa.setText(ma);
        this.txtTen.setText(ten);
        this.cbbdsp.setSelectedItem(trangThai);
    }//GEN-LAST:event_tbldspMouseClicked

