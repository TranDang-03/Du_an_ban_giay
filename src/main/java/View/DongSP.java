    public void loadTable() {
        ArrayList<Model.DongSp> list = this.service.all();
        model.setRowCount(0);

        for (Model.DongSp dsp : list) {
            model.addRow(new Object[]{
                dsp.getIdDong(),
                dsp.getMaDong(),
                dsp.getTenDong(),
                dsp.getTrangThai() == 0 ? "Hoạt động" : "Không hoạt động"
            });
        }
    }

    public void clear() {
        this.txtID.setText("");
        this.txtMa.setText("");
        this.txtTen.setText("");
        this.cbbdsp.setSelectedItem(true);
    }

