    private void tblclMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblclMouseClicked
        int index = this.tblcl.getSelectedRow();

        String id = model.getValueAt(index, 0).toString();
        String ma = model.getValueAt(index, 1).toString();
        String daChinh = model.getValueAt(index, 2).toString();
        String daPhu = model.getValueAt(index, 3).toString();
        String daNgoai = model.getValueAt(index, 4).toString();
        String lopLotTrong = model.getValueAt(index, 5).toString();
        String trangThai = model.getValueAt(index, 6).toString();

        this.txtID.setText(id);
        this.txtMaCl.setText(ma);
        this.txtDaChinhs.setText(daChinh);
        this.txtDaPhu.setText(daPhu);
        this.txtDaNGoai.setText(daNgoai);
        this.txtLopLot.setText(lopLotTrong);
        this.cbbcl.setSelectedItem(trangThai);
    }//GEN-LAST:event_tblclMouseClicked

