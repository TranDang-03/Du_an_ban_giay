    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        String maChatLieu = this.txtMaCl.getText();
        String daChinh = this.txtDaChinhs.getText();
        String daPhu = this.txtDaPhu.getText();
        String daNgoai = this.txtDaNGoai.getText();
        String lopLotTrong = this.txtLopLot.getText();
        int trangThai = Integer.parseInt(cbbcl.getSelectedItem().toString());

        Model.ChatLieu cl = new Model.ChatLieu(0, maChatLieu, daChinh, daPhu, daNgoai, lopLotTrong, trangThai);

        boolean a = this.service.insert(cl);

        if (a == true) {
            JOptionPane.showMessageDialog(rootPane, "Thêm thành công");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Thêm thất bại");
            return;
        }

        this.clear();
        this.loadTable();
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int id = Integer.parseInt(txtID.getText());
       String maChatLieu = this.txtMaCl.getText();
        String daChinh = this.txtDaChinhs.getText();
        String daPhu = this.txtDaPhu.getText();
        String daNgoai = this.txtDaNGoai.getText();
        String lopLotTrong = this.txtLopLot.getText();
        int trangThai = Integer.parseInt(cbbcl.getSelectedItem().toString());

        Model.ChatLieu cl = new Model.ChatLieu(id, maChatLieu, daChinh, daPhu, daNgoai, lopLotTrong, trangThai);
//
        boolean s = this.service.update(cl);

        if (s == true) {
            JOptionPane.showMessageDialog(rootPane, "Sửa thành công");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Sửa thất bại");
            return;
        }

        this.clear();
        this.loadTable();
    }//GEN-LAST:event_btnSuaActionPerformed

