    public void loadTable() {
        ArrayList<Model.ChatLieu> list = this.service.all();
        model.setRowCount(0);

        for (Model.ChatLieu cl : list) {
            model.addRow(new Object[]{
                cl.getIdCL(),
                cl.getMaCL(),
                cl.getDaChinh(),
                cl.getDaPhu(),
                cl.getDaNgoai(),
                cl.getLopLot(),
                cl.getTrangThai() == 0 ? "Hoạt động" : "Không hoạt động"
            });
        }
    }

